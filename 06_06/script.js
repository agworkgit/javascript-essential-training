// String:
let stringDemo = "A string of text.";
console.log("String:", stringDemo);
console.log(typeof stringDemo);

// Numbes:
let integerDemo = 4;
console.log("Integer:", typeof integerDemo);
console.log(typeof integerDemo);

let floatDemo = 5.6;
console.log("Floating point number:", floatDemo);
console.log(typeof floatDemo);

let largeIntDemo = 54564895068093840598304985987348573453453746583746345345345345345n;
console.log("Big integer number:", largeIntDemo);
console.log(typeof largeIntDemo);


// Boolean:
let booleanDemo = true;
console.log("Boolean value:", booleanDemo);
console.log(typeof booleanDemo);

// Null value (nothing):
let nullDemo = null;
console.log("Null value:", nullDemo);
console.log(typeof nullDemo);

// Undefined:
let undefinedDemo;
console.log("Undefined:", undefinedDemo);
console.log(typeof undefinedDemo);

let undefinedAssignedDemo = undefined;
console.log("Undefined assigned:", undefinedAssignedDemo);
console.log(typeof undefinedAssignedDemo);

// Object:
const objectDemo = {
  dance: "Mambo",
  number: 5,
};
console.log("Object:", objectDemo);
console.log(typeof objectDemo);

// Array:
const arrayDemo = ["tango", "foxtrot", "waltz", "rumba", "bolero"];
console.log("Array:", arrayDemo);
console.log(typeof arrayDemo);